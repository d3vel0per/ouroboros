@dataclass(frozen=True, slots=True)
class DetectedCommands:
    """Validated command proposals ready for persistence."""

    lint: str | None = None
    build: str | None = None
    test: str | None = None
    static: str | None = None
    coverage: str | None = None

    def is_empty(self) -> bool:
        return all(
            getattr(self, key) is None for key in ("lint", "build", "test", "static", "coverage")
        )


def toml_path(working_dir: Path) -> Path:
    """Return the canonical path to ``.ouroboros/mechanical.toml``."""
    return working_dir / _TOML_RELATIVE_PATH


def has_mechanical_toml(working_dir: Path) -> bool:
    """Return True iff ``.ouroboros/mechanical.toml`` already exists."""
    return toml_path(working_dir).is_file()


async def ensure_mechanical_toml(
    working_dir: Path,
    adapter: LLMAdapter,
    *,
    model: str | None = None,
    backend: str | None = None,
    force: bool = False,
) -> bool:
    """Write ``.ouroboros/mechanical.toml`` via one AI detect call.

    Idempotent: when the toml already exists and ``force`` is False, returns
    True immediately. Otherwise makes one LLM call, validates the proposals,
    and persists the file. Never raises — returns False on any failure.

    Args:
        working_dir: Project root to detect against.
        adapter: LLM adapter used for the single detect call.
        model: Explicit model identifier. When ``None``, resolved via
            ``get_mechanical_detector_model(backend)`` so Codex / OpenCode
            backends receive the ``"default"`` sentinel instead of a Claude
            model name they cannot route.
        backend: Optional backend hint used when ``model`` is ``None``.
        force: If True, re-detect and overwrite an existing toml.

    Returns:
        True on success (toml exists after the call), False otherwise.
    """
    model_is_explicit = model is not None
    if model is None:
        from ouroboros.config.loader import get_mechanical_detector_model

        model = get_mechanical_detector_model(backend=backend)
    target = toml_path(working_dir)
    if target.is_file() and not force:
        return True

    manifests = _collect_manifests(working_dir)
    if not manifests:
        log.info("detector.skipped", reason="no_manifests", working_dir=str(working_dir))
        return False

    proposal = await _ask_llm(
        adapter,
        working_dir,
        manifests,
        model,
        model_is_explicit=model_is_explicit,
    )
    if proposal is None:
        return False

    validated = _validate_proposal(working_dir, proposal)
    if validated.is_empty():
        log.info(
            "detector.empty_after_validation",
            working_dir=str(working_dir),
            proposal=_redact(proposal),
        )
        return False

    # Atomic replace: write the new toml to a sibling file first and
    # ``os.replace`` it over the existing one. This keeps the prior
    # known-good file intact if rendering or writing fails on any
    # transient error — ``ouroboros detect --force`` never destroys the
    # last working config before a replacement is ready.
    temp = target.with_name(target.name + ".tmp")
    try:
        target.parent.mkdir(parents=True, exist_ok=True)
        temp.write_text(_render_toml(validated), encoding="utf-8")
        os.replace(temp, target)
    except OSError as exc:
        log.warning("detector.write_failed", path=str(target), error=str(exc))
        try:
            if temp.exists():
                temp.unlink()
        except OSError:
            pass
        return False

    log.info(
        "detector.toml_written",
        path=str(target),
        commands={k: getattr(validated, k) for k in _COMMAND_KEYS},
    )
    return True


# Glob patterns for manifests whose name varies per project (common in
# .NET repos where ``*.csproj`` / ``*.sln`` / ``*.fsproj`` stand in for a
# top-level manifest). Listed separately from ``_MANIFEST_CANDIDATES`` so
# the cheap exact-name lookup stays the hot path.
_MANIFEST_GLOB_PATTERNS: tuple[str, ...] = (
    "*.csproj",
    "*.sln",
    "*.fsproj",
    "*.vbproj",
)

# Subdirectory patterns where monorepo projects commonly live. Both literal
# names and glob patterns are walked so ``backend/pyproject.toml`` and
# ``packages/web/package.json`` can seed the detect call even when the
# root has no manifest of its own.
_MONOREPO_SUBDIR_NAMES: tuple[str, ...] = (
    "backend",
    "frontend",
    "api",
    "app",
    "client",
    "server",
    "web",
    "core",
    "service",
)
_MONOREPO_SUBDIR_GLOBS: tuple[str, ...] = (
    "packages/*",
    "apps/*",
    "services/*",
    "crates/*",
    "src/*",
)
# Cap on the number of subpath manifest entries forwarded to the LLM so a
# wide monorepo cannot blow the prompt budget. Root manifests always land
# first; subdirectory extras are appended until the cap is hit.
_MAX_SUBDIR_MANIFESTS = 12


def _collect_manifests(working_dir: Path) -> dict[str, str]:
    """Collect root and monorepo-subdir manifests for the detect prompt.

    Root manifests (by exact name or glob) are always included. Well-known
    monorepo subdirectories (``backend/``, ``packages/*``, ``apps/*`` …)
    are also scanned so subproject manifests like
    ``backend/pyproject.toml`` or ``packages/web/package.json`` can seed
    the LLM call. The return dict preserves relative paths in its keys
    (``"backend/pyproject.toml"``) so the prompt renders them verbatim,
    and the subdir sweep is capped at ``_MAX_SUBDIR_MANIFESTS`` entries
    to keep the prompt bounded.
    """
    collected: dict[str, str] = {}
    for name in _MANIFEST_CANDIDATES:
        candidate = working_dir / name
        if not candidate.is_file():
            continue
        try:
            raw = candidate.read_bytes()[:_MANIFEST_EXCERPT_BYTES]
            collected[name] = raw.decode("utf-8", errors="replace")
        except OSError:
            continue
    for pattern in _MANIFEST_GLOB_PATTERNS:
        try:
            for path in working_dir.glob(pattern):
                if not path.is_file() or path.name in collected:
                    continue
                try:
                    raw = path.read_bytes()[:_MANIFEST_EXCERPT_BYTES]
                except OSError:
                    continue
                collected[path.name] = raw.decode("utf-8", errors="replace")
        except OSError:
            continue
    remaining = _MAX_SUBDIR_MANIFESTS
    for subdir in _iter_monorepo_subdirs(working_dir):
        if remaining <= 0:
            break
        remaining = _append_subdir_manifests(working_dir, subdir, collected, remaining)
    return collected


def _append_subdir_manifests(
    root: Path,
    subdir: Path,
    collected: dict[str, str],
    remaining: int,
) -> int:
    for manifest_name in _MANIFEST_CANDIDATES:
        if remaining <= 0:
            return remaining
        candidate = subdir / manifest_name
        if not candidate.is_file():
            continue
        key = candidate.relative_to(root).as_posix()
        if key in collected:
            continue
        try:
            raw = candidate.read_bytes()[:_MANIFEST_EXCERPT_BYTES]
        except OSError:
            continue
        collected[key] = raw.decode("utf-8", errors="replace")
        remaining -= 1
    for pattern in _MANIFEST_GLOB_PATTERNS:
        if remaining <= 0:
            return remaining
        try:
            for path in subdir.glob(pattern):
                if remaining <= 0:
                    return remaining
                if not path.is_file():
                    continue
                key = path.relative_to(root).as_posix()
                if key in collected:
                    continue
                try:
                    raw = path.read_bytes()[:_MANIFEST_EXCERPT_BYTES]
                except OSError:
                    continue
                collected[key] = raw.decode("utf-8", errors="replace")
                remaining -= 1
        except OSError:
            continue
    return remaining


def _iter_monorepo_subdirs(working_dir: Path):
    for name in _MONOREPO_SUBDIR_NAMES:
        candidate = working_dir / name
        if candidate.is_dir():
            yield candidate
    for glob in _MONOREPO_SUBDIR_GLOBS:
        try:
            for candidate in working_dir.glob(glob):
                if candidate.is_dir():
                    yield candidate
        except OSError:
            continue


_SYSTEM_PROMPT = """You are inspecting a software repository to propose the commands Ouroboros should run as zero-cost Stage 1 verification (lint, build, test, static analysis, coverage).

Rules:
1. Propose only commands that this project can actually execute right now, based on the manifest excerpts provided. Do not invent scripts that are not declared.
2. Prefer the project's own conventions: npm/pnpm/yarn scripts, uv/pytest, cargo, go, make, just, etc.
3. If a category (e.g. coverage) has no clear command, OMIT it from the JSON — do not guess.
4. Every command must start with a well-known tool. Absolute paths and shell operators (`&&`, `||`, pipes, redirects) are forbidden.
5. Output ONLY a JSON object with at most the keys: lint, build, test, static, coverage. Values are single-line shell command strings."""


async def _ask_llm(
    adapter: LLMAdapter,
    working_dir: Path,
    manifests: dict[str, str],
    model: str,
    *,
    model_is_explicit: bool = False,
) -> dict[str, str] | None:
    """Send one detect prompt and return a parsed JSON proposal."""
    payload_parts = [f"Working directory: {working_dir.name}", ""]
    for name, body in manifests.items():
        payload_parts.append(f"--- {name} ---")
        payload_parts.append(body.rstrip())
        payload_parts.append("")
    payload_parts.append(
        "Return a JSON object mapping at most {lint, build, test, static, coverage} to"
        " shell command strings. Omit keys you cannot verify."
    )

    messages = [
        Message(role=MessageRole.SYSTEM, content=_SYSTEM_PROMPT),
        Message(role=MessageRole.USER, content="\n".join(payload_parts)),
    ]
    config = CompletionConfig(
        model=model,
        role="mechanical_detection",
        model_is_explicit=model_is_explicit,
        temperature=0.0,
        max_tokens=512,
        response_format={"type": "json_object"},
    )
    result = await adapter.complete(messages, config)
    if result.is_err:
        log.warning("detector.llm_failed", error=str(result.error))
        return None

    raw = extract_json_payload(result.value.content)
    if raw is None:
        log.warning(
            "detector.llm_unparseable",
            preview=(result.value.content or "")[:200],
        )
        return None
    try:
        payload = json.loads(raw)
    except json.JSONDecodeError:
        return None
    if not isinstance(payload, dict):
        return None

    clean: dict[str, str] = {}
    for key in _COMMAND_KEYS:
        value = payload.get(key)
        if isinstance(value, str) and value.strip():
            clean[key] = value.strip()
    return clean


def _validate_proposal(
    working_dir: Path,
    proposal: dict[str, str],
) -> DetectedCommands:
    """Drop commands whose entry point cannot be verified on disk."""
    kept: dict[str, str | None] = dict.fromkeys(_COMMAND_KEYS)
    for key, command in proposal.items():
        if key not in _COMMAND_KEYS:
            continue
        if _command_is_valid(working_dir, command):
            kept[key] = command
        else:
            log.info(
                "detector.command_dropped",
                key=key,
                command=command,
                working_dir=str(working_dir),
            )
    return DetectedCommands(**kept)


_FORBIDDEN_TOKENS: tuple[str, ...] = ("&&", "||", "|", ";", ">", "<", "`", "$(")


# Target / recipe / task names that strongly imply the command mutates
# external state: CI deploy/publish/release workflows, gem binaries like
# ``capistrano``, destructive clean / reset flows, etc. Any user-named
# target that matches this set is refused even when it exists in the
# project's Makefile / justfile / Taskfile / build.gradle / Rakefile,
# because Stage 1 is supposed to be zero-cost verification. Users who
# *really* want to run these as Stage 1 can author them into
# ``mechanical.toml`` by hand.
_DESTRUCTIVE_TARGET_NAMES: frozenset[str] = frozenset(
    {
        "deploy",
        "deployment",
        "redeploy",
        "release",
        "publish",
        "publish-package",
        "publishpackage",
        "push",
        "upload",
        "ship",
        "install",
        "uninstall",
        "nuget-push",
        "pypi-upload",
        "docker-push",
        "docker-publish",
        "container-push",
        "prod-deploy",
        "production-deploy",
        "staging-deploy",
        "purge",
        "reset",
        "capistrano",
        "cap",
    }
)


def _is_destructive_target(name: str) -> bool:
    """True when ``name`` looks like a state-changing pipeline step."""
    lowered = name.lower()
    if lowered in _DESTRUCTIVE_TARGET_NAMES:
        return True
    # Catch common suffix / prefix patterns (``deploy-staging``,
    # ``publish-docker`` …). A single bare ``-`` split is enough; we don't
    # want to be over-aggressive and reject ``test-integration``.
    return any(token in _DESTRUCTIVE_TARGET_NAMES for token in lowered.replace("_", "-").split("-"))


def _command_is_valid(working_dir: Path, command: str) -> bool:
    """Return True iff ``command`` is safely runnable from ``working_dir``.

    Checks:
    1. No shell chaining / redirects (Stage 1 runs via ``create_subprocess_exec``).
    2. The executable is not a path that escapes ``working_dir``: absolute
       paths (``/tmp/...``), home-relative paths (``~/...``), and
       parent-directory escapes (``../..``) are refused. Only bare names
       and ``./``-prefixed project-local wrappers survive.
    3. The executable basename is on the curated allowlist.
    4. Script-style invocations (``npm run <name>``, ``make <target>``,
       ``just <recipe>`` …) reference a target declared in the project's
       manifest, and bare tools (``pytest``, ``eslint`` …) must be
       declared by the repo itself. Host ``PATH`` lookup is intentionally
       not consulted.
    """
    if any(token in command for token in _FORBIDDEN_TOKENS):
        return False

    try:
        import shlex

        parts = shlex.split(command, posix=(os.name != "nt"))
    except ValueError:
        return False
    if not parts:
        return False

    raw_head = parts[0]
    if raw_head.startswith(("/", "~")) or ".." in Path(raw_head).parts:
        return False
    # ``./mvnw`` is fine, but any other path that points outside the cwd
    # component (e.g. ``bin/foo`` pointing at a committed shim) would still
    # need its basename to match the allowlist.
    head = Path(raw_head).name
    if head not in _ALLOWED_EXECUTABLES:
        return False
    # When the proposal uses a path prefix (``bin/pytest``, ``tools/eslint``,
    # ``./mvnw``), require the file to actually exist in the repo. Otherwise
    # ``create_subprocess_exec`` would later fail with a phantom "no such
    # file" error at Stage 1 time, which this detector is supposed to
    # prevent.
    if "/" in raw_head and not (working_dir / raw_head).is_file():
        return False

    if not _arguments_stay_within_repo(working_dir, head, parts[1:]):
        return False

    return _verify_entry_point(working_dir, head, parts)


def _arguments_stay_within_repo(working_dir: Path, head: str, args: list[str]) -> bool:
    """Reject path-like arguments that can point outside ``working_dir``."""

    for token in args:
        if head == "bazel" and _is_bazel_label(token):
            continue
        for candidate in _argument_path_candidates(token):
            if _path_argument_escapes_repo(working_dir, candidate):
                return False
    return True


def _argument_path_candidates(token: str) -> tuple[str, ...]:
    """Return raw argument values that could be interpreted as filesystem paths."""

    if not token or token == "--":
        return ()
    if "=" in token:
        value = token.split("=", 1)[1]
        return (value,) if value else ()
    return (token,)


def _path_argument_escapes_repo(working_dir: Path, value: str) -> bool:
    """Return True when an argument path is absolute or resolves outside repo."""

    if not value or value == "--":
        return False
    if value.startswith("~"):
        return True
    path = Path(value)
    if path.is_absolute() or PureWindowsPath(value).is_absolute():
        return True
    if ".." in path.parts or ".." in PureWindowsPath(value).parts:
        return True
    if _looks_like_path(working_dir, value):
        candidate = (working_dir / value).resolve()
        if not _path_is_within(working_dir, candidate):
            return True
    return False


def _looks_like_path(working_dir: Path, value: str) -> bool:
    """Heuristic for path-ish args; non-path flags and names are ignored."""

    if value in {".", "./"}:
        return True
    if "/" in value or "\\" in value:
        return True
    if value.startswith("."):
        return True
    return (working_dir / value).exists()


def _is_bazel_label(value: str) -> bool:
    """True for Bazel target labels such as ``//...`` or ``@repo//pkg:target``."""

    return value.startswith("//") or (value.startswith("@") and "//" in value)


def _verify_entry_point(
    working_dir: Path,
    head: str,
    parts: list[str],
) -> bool:
    """Confirm the script/target referenced by ``parts`` exists on disk.

    Bare binaries must resolve on PATH. Script runners must reference a
    declared script or target.
    """
    if head == "bun":
        # ``bun`` is self-contained: require Bun project evidence, then
        # dispatch to either the runtime-builtin, ``bun x`` (npx-like), or
        # script-lookup path. The function returns unconditionally from this
        # branch so the shared Node script-lookup below is not revisited and
        # a valid ``bun test`` cannot be rejected for missing a
        # ``scripts.test`` entry.
        if not _is_bun_project(working_dir):
            return False
        if len(parts) >= 2 and parts[1] == "x":
            # ``bun x`` is network-fetch equivalent to ``npx``. Require the
            # package to be declared (or locally installed) so Stage 1 cannot
            # silently execute untrusted remote packages.
            package = _npx_package(parts[1:])
            if package is None:
                return False
            return _npx_package_available(working_dir, package)
        if _bun_builtin_runner(parts):
            return True
        # Custom Bun script like ``bun myscript`` → falls back to the
        # ``package.json.scripts`` contract.
        script_name = _node_script_name("bun", parts)
        if script_name is None:
            return False
        return _package_json_has_script(working_dir, script_name)

    if head in {"npm", "pnpm", "yarn"}:
        pm_working_dir = _node_pm_working_dir(working_dir, parts)
        if pm_working_dir is None:
            return False
        script_name = _node_script_name(head, parts)
        if script_name is None:
            return False
        workspace = _node_workspace_filter(head, parts)
        return _node_workspace_has_script(pm_working_dir, workspace, script_name)

    if head == "npx":
        package = _npx_package(parts)
        if package is None:
            return False
        return _npx_package_available(working_dir, package)

    if head == "make":
        if not _has_any(working_dir, ("Makefile", "makefile", "GNUmakefile")):
            return False
        target = _make_target(parts)
        if target is None:
            # Bare ``make`` runs whatever happens to be the first rule in the
            # Makefile, which may well be ``install`` or ``deploy``. We cannot
            # verify that from outside, so the command is dropped. Users who
            # want the default target can name it explicitly (``make all``).
            return False
        if _is_destructive_target(target):
            return False
        return _makefile_has_target(working_dir, target)

    if head == "just":
        recipe = _first_positional(parts)
        if recipe is None:
            return False
        if _is_destructive_target(recipe):
            return False
        return _justfile_has_recipe(working_dir, recipe)

    if head == "uv":
        # Manifest presence is delegated to ``_uv_subcommand_is_available``
        # so that ``uv run --directory backend`` can validate against the
        # subdirectory's ``pyproject.toml`` in monorepo layouts.
        return _uv_subcommand_is_available(working_dir, parts)

    if head == "cargo":
        if not (working_dir / "Cargo.toml").is_file() and not _cargo_manifest_path_exists(
            working_dir, parts
        ):
            return False
        return _cargo_subcommand_is_available(parts)

    if head == "go":
        if not (working_dir / "go.mod").is_file():
            return False
        return _go_subcommand_is_builtin(parts)

    if head == "zig":
        if not (working_dir / "build.zig").is_file():
            return False
        return _zig_subcommand_is_builtin(parts)

    if head == "mvn":
        if not (working_dir / "pom.xml").is_file():
            return False
        return _maven_goals_are_safe(parts)

    if head in {"mvnw", "mvnw.cmd"}:
        # Maven wrapper scripts must be invoked with the ``./`` prefix —
        # ``create_subprocess_exec`` does not search the current directory
        # and bare ``mvnw`` would fail to resolve on Unix. ``.cmd`` is only
        # runnable on Windows; on POSIX it is rejected outright.
        if not _wrapper_invocation_is_runnable(parts[0], head):
            return False
        if not (working_dir / "pom.xml").is_file():
            return False
        if not (working_dir / head).is_file():
            return False
        return _maven_goals_are_safe(parts)

    if head in {"gradlew", "gradlew.bat"}:
        if not _wrapper_invocation_is_runnable(parts[0], head):
            return False
        if not (
            (working_dir / "build.gradle").is_file() or (working_dir / "build.gradle.kts").is_file()
        ):
            return False
        if not (working_dir / head).is_file():
            return False
        return _gradle_task_is_declared(working_dir, parts)

    if head == "gradle":
        if not (
            (working_dir / "build.gradle").is_file() or (working_dir / "build.gradle.kts").is_file()
        ):
            return False
        return _gradle_task_is_declared(working_dir, parts)

    if head in {"python", "python3"}:
        return _python_module_is_available(working_dir, parts)

    if head in {"poetry", "pdm", "hatch"}:
        if not (working_dir / "pyproject.toml").is_file():
            return False
        return _python_pm_run_tool_is_declared(working_dir, parts)

    if head == "task":
        return _task_task_is_declared(working_dir, parts)

    if head == "rake":
        return _rake_task_is_declared(working_dir, parts)

    if head == "bundle":
        return _bundle_subcommand_is_safe(working_dir, parts)

    if head == "composer":
        return _composer_script_is_declared(working_dir, parts)

    if head == "mix":
        return _mix_task_is_builtin(working_dir, parts)

    if head in _REPO_COUPLED_RUNNERS:
        return _REPO_COUPLED_RUNNERS[head](working_dir, parts)

    # Final fallback: the tool must be provably installable from the repo
    # state — declared in a Python / Node manifest or already vendored into a
    # project-local bin directory. Falling back to a host-wide ``shutil.which``
    # lookup would let an LLM hallucination become a phantom Stage 1 failure
    # on any machine that happens to have the binary installed, so that path
    # is intentionally removed.
    return _bare_tool_declared_by_repo(working_dir, head)


