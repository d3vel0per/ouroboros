# that is not in ``_UV_RUN_VALUE_OPTIONS`` is treated as boolean — the
# parser skips one token. ``--flag=value`` is always self-contained.


def _uv_run_tool(parts: list[str]) -> str | None:
    """Return the tool name in a ``uv run ...`` invocation, or None.

    Properly skips ``--flag value`` pairs listed in ``_UV_RUN_VALUE_OPTIONS``
    and the self-contained ``--flag=value`` form. ``-m`` / ``--module``
    is special-cased: the following token is the module being invoked
    (``uv run -m pytest``), so the validator treats that token as the
    "tool" just like the bare form ``uv run pytest``.
    """
    try:
        run_idx = parts.index("run")
    except ValueError:
        return None
    i = run_idx + 1
    while i < len(parts):
        token = parts[i]
        if not token.startswith("-"):
            return token
        if token in {"-m", "--module"} and i + 1 < len(parts):
            return parts[i + 1]
        if token.startswith(("--module=", "-m=")):
            return token.split("=", 1)[1]
        if "=" in token:
            i += 1
            continue
        if token in _UV_RUN_VALUE_OPTIONS:
            i += 2
            continue
        i += 1
    return None


def _uv_project_root(working_dir: Path, parts: list[str]) -> Path | None:
    """Return the project root that ``uv`` will operate against.

    ``uv run --directory backend pytest`` / ``uv --project backend run pytest``
    relocate the effective project root. Both ``--flag value`` and
    ``--flag=value`` forms are honoured; the path is resolved relative to
    ``working_dir`` and must stay inside it. An escape attempt
    (``--directory ../other``) returns ``None`` so the caller can reject
    the command — Stage 1 must not validate against a sibling checkout
    outside the repo under evaluation.
    """
    for flag in ("--directory", "--project"):
        for i, token in enumerate(parts):
            if token == flag and i + 1 < len(parts):
                return _resolve_within(working_dir, parts[i + 1])
            prefix = f"{flag}="
            if token.startswith(prefix):
                return _resolve_within(working_dir, token[len(prefix) :])
    return working_dir


def _resolve_within(working_dir: Path, relative: str) -> Path | None:
    """Resolve ``relative`` against ``working_dir``, or None if it escapes.

    ``working_dir`` itself is also resolved so symlinked repo roots still
    match. Absolute paths, ``~`` references, and ``..`` segments that
    land outside the root all return ``None``.
    """
    if relative.startswith(("/", "~")):
        return None
    candidate = (working_dir / relative).resolve()
    if not _path_is_within(working_dir, candidate):
        return None
    return candidate


def _path_is_within(working_dir: Path, candidate: Path) -> bool:
    """Return True when ``candidate`` resolves inside ``working_dir``."""

    root = working_dir.resolve()
    try:
        candidate.resolve().relative_to(root)
    except ValueError:
        return False
    return True


def _relative_glob_stays_within(pattern: str) -> bool:
    """Return False for workspace globs that can enumerate outside the repo."""

    if pattern.startswith("~"):
        return False
    path = Path(pattern)
    if path.is_absolute():
        return False
    return ".." not in path.parts


def _uv_subcommand(parts: list[str]) -> str | None:
    """Return the ``uv`` subcommand, skipping global flag / value pairs.

    ``uv --project backend run pytest`` and ``uv --directory=backend run
    pytest`` both resolve to ``run``. ``--flag=value`` is self-contained;
    ``--flag value`` pairs consume two tokens. Boolean flags like
    ``--frozen`` / ``--quiet`` / ``--verbose`` consume one token.
    """
    i = 1  # skip ``uv`` itself
    while i < len(parts):
        token = parts[i]
        if not token.startswith("-"):
            return token
        if "=" in token:
            i += 1
            continue
        if token in _UV_RUN_VALUE_OPTIONS:
            i += 2
            continue
        i += 1
    return None


def _uv_subcommand_is_available(working_dir: Path, parts: list[str]) -> bool:
    """Validate ``uv`` invocations by inspecting the sub-tool they invoke.

    ``uv run <tool>`` is the common pattern; we require ``<tool>`` to exist in
    a venv entry (``.venv/bin/<tool>``), on PATH, or to be declared in
    ``pyproject.toml`` dependencies so the repo can actually install it.
    When ``uv run --with <pkg>`` provisions a package just for the run, the
    packaged tool is treated as declared. Other ``uv`` subcommands
    (``uv sync``, ``uv build``, ``uv pip install`` …) are accepted by
    presence of the manifest alone — they do not invoke a user-controlled
    tool name.

    ``--directory`` / ``--project`` retarget the effective project root so
    monorepo layouts like ``uv run --directory backend pytest`` validate
    against the Python project that actually lives under ``backend/``.
    The same flags are honoured in ``uv --project backend run pytest``
    form (global option before the subcommand).
    """
    sub = _uv_subcommand(parts)
    if sub is None:
        return False
    if sub != "run":
        if sub not in _UV_NON_RUN_SUBCOMMANDS:
            return False
        # Even read-only subcommands (``uv tree`` / ``uv version``) require a
        # Python project in the target directory — otherwise ``uv`` will
        # either fail to resolve metadata or operate on the wrong cwd.
        project_root = _uv_project_root(working_dir, parts)
        if project_root is None:
            return False
        return (project_root / "pyproject.toml").is_file() or (project_root / "uv.lock").is_file()
    tool = _uv_run_tool(parts)
    if tool is None:
        return False
    project_root = _uv_project_root(working_dir, parts)
    if project_root is None:
        return False
    if not ((project_root / "pyproject.toml").is_file() or (project_root / "uv.lock").is_file()):
        return False
    # Repo-coupled acceptance paths only. A host-wide ``shutil.which`` lookup
    # used to be accepted here, but that made a developer machine's globally
    # installed ``pyright`` / ``ruff`` / ``pytest`` enough to persist
    # ``uv run <tool>`` commands the project cannot actually run in CI.
    if (project_root / ".venv" / "bin" / tool).exists():
        return True
    if (project_root / ".venv" / "Scripts" / f"{tool}.exe").exists():
        return True
    if _pyproject_declares_dependency(project_root, tool):
        return True
    if _requirements_files_declare(project_root, tool):
        return True
    # ``uv run --with <pkg>`` / ``--with-editable <pkg>`` provisions the
    # package for the duration of the run, so the tool is runnable even when
    # it is not declared in the repo manifests.
    return _uv_with_supplies_tool(parts, tool)


def _uv_with_supplies_tool(parts: list[str], tool: str) -> bool:
    """True when ``--with <pkg>`` / ``--with-editable`` declares ``tool``."""
    lowered = tool.lower()
    i = 0
    while i < len(parts):
        token = parts[i]
        candidate: str | None = None
        if token in {"--with", "--with-editable"} and i + 1 < len(parts):
            candidate = parts[i + 1]
            i += 2
        elif token.startswith(("--with=", "--with-editable=")):
            candidate = token.split("=", 1)[1]
            i += 1
        else:
            i += 1
            continue
        if candidate is None:
            continue
        name = candidate.strip()
        for sep in "[<>=!~ ":
            idx = name.find(sep)
            if idx != -1:
                name = name[:idx]
                break
        if name.lower() == lowered:
            return True
    return False


def _pyproject_declares_dependency(working_dir: Path, name: str) -> bool:
    """Return True iff ``name`` appears as a declared dependency in pyproject.toml.

    Covers both modern and legacy layouts so Poetry-managed repos keep Stage 1
    coverage:

    * PEP 621 ``[project.dependencies]`` / ``[project.optional-dependencies]``
    * PEP 735 ``[dependency-groups.*]``
    * ``[tool.uv.dev-dependencies]``
    * ``[tool.poetry.dependencies]``, ``[tool.poetry.dev-dependencies]``, and
      ``[tool.poetry.group.<name>.dependencies]`` (Poetry 1.2+ layout)
    """
    path = working_dir / "pyproject.toml"
    if not path.is_file():
        return False
    try:
        import tomllib

        with open(path, "rb") as handle:
            data = tomllib.load(handle)
    except (OSError, ValueError):
        return False
    list_haystacks: list[object] = []
    mapping_haystacks: list[object] = []
    project = data.get("project") if isinstance(data, dict) else None
    if isinstance(project, dict):
        list_haystacks.append(project.get("dependencies"))
        optional = project.get("optional-dependencies")
        if isinstance(optional, dict):
            list_haystacks.extend(optional.values())
    dep_groups = data.get("dependency-groups") if isinstance(data, dict) else None
    if isinstance(dep_groups, dict):
        list_haystacks.extend(dep_groups.values())
    tool = data.get("tool") if isinstance(data, dict) else None
    if isinstance(tool, dict):
        uv_section = tool.get("uv")
        if isinstance(uv_section, dict):
            list_haystacks.append(uv_section.get("dev-dependencies"))
        poetry = tool.get("poetry")
        if isinstance(poetry, dict):
            mapping_haystacks.append(poetry.get("dependencies"))
            mapping_haystacks.append(poetry.get("dev-dependencies"))
            groups = poetry.get("group")
            if isinstance(groups, dict):
                for entry in groups.values():
                    if isinstance(entry, dict):
                        mapping_haystacks.append(entry.get("dependencies"))
    lowered = name.lower()
    # Poetry-style tables use dep-name keys, so check those first via direct
    # key lookup to avoid a list scan.
    for bucket in mapping_haystacks:
        if isinstance(bucket, dict):
            for key in bucket:
                if isinstance(key, str) and key.lower() == lowered:
                    return True
    for bucket in list_haystacks:
        if not isinstance(bucket, list):
            continue
        for raw in bucket:
            if not isinstance(raw, str):
                continue
            token = raw.strip().split(";", 1)[0]
            for sep in "[<>=!~ ":
                idx = token.find(sep)
                if idx != -1:
                    token = token[:idx]
                    break
            if token.strip().lower() == lowered:
                return True
    return False


