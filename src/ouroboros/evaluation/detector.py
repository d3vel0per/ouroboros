def _strip_pm_flags(head: str, parts: list[str]) -> list[str]:
    """Return ``parts`` with leading package-manager flag pairs dropped.

    Handles ``--filter <name>`` / ``--workspace=<name>`` / ``-w <name>``,
    ``yarn workspace <name>``, and ``yarn workspaces foreach [flags] <script>``
    so downstream checks see a clean ``<pm> [run] <script>`` shape.
    """
    out: list[str] = [parts[0]]
    i = 1
    while i < len(parts):
        token = parts[i]
        if not token.startswith("-"):
            break
        if "=" in token:
            i += 1
            continue
        if token in _NODE_PM_VALUE_FLAGS:
            i += 2
            continue
        i += 1
    if head == "yarn" and i < len(parts) and parts[i] in _YARN_WORKSPACE_KEYWORDS:
        keyword = parts[i]
        i += 1
        if keyword == "workspaces" and i < len(parts) and parts[i] == "foreach":
            # ``yarn workspaces foreach [flags] <script>`` — skip ``foreach``
            # plus any of its flag/value pairs so the next positional is the
            # script being invoked across workspaces.
            i += 1
            while i < len(parts):
                token = parts[i]
                if not token.startswith("-"):
                    break
                if "=" in token:
                    i += 1
                    continue
                if token in _YARN_FOREACH_VALUE_FLAGS:
                    i += 2
                    continue
                i += 1
        elif i < len(parts):
            # ``yarn workspace <name> <script>``: skip the workspace name.
            i += 1
    out.extend(parts[i:])
    return out


def _node_pm_working_dir(working_dir: Path, parts: list[str]) -> Path | None:
    """Return the effective cwd for npm/pnpm/yarn, rejecting escapes."""

    for i, token in enumerate(parts):
        if token in _NODE_PM_PATH_FLAGS:
            if i + 1 >= len(parts):
                return None
            return _resolve_within(working_dir, parts[i + 1])
        for flag in _NODE_PM_PATH_FLAGS:
            prefix = f"{flag}="
            if token.startswith(prefix):
                return _resolve_within(working_dir, token[len(prefix) :])
    return working_dir


def _node_script_name(head: str, parts: list[str]) -> str | None:
    """Return the script name a Node package manager will resolve from ``parts``.

    Leading flag pairs (``--filter <name>``, ``--workspace=<name>`` …) are
    skipped so workspace-scoped commands like ``pnpm --filter web test`` keep
    resolving to the ``test`` script. Bare tokens after ``<pm>`` are treated
    as implicit scripts (``yarn typecheck``, ``pnpm check`` …); explicit
    ``<pm> run <name>`` is also supported. Package-manager built-ins (``npm
    install``, ``pnpm publish`` …) return ``None`` so the caller can reject
    the command rather than execute something unverified.
    """
    cleaned = _strip_pm_flags(head, parts)
    if len(cleaned) < 2:
        return None
    second = cleaned[1]
    if second.startswith("-"):
        return None
    if second == "run":
        if len(cleaned) < 3 or cleaned[2].startswith("-"):
            return None
        return cleaned[2]
    if head == "npm":
        # npm only treats a small set of lifecycle names as script shortcuts;
        # everything else must go through ``npm run``.
        if second in _NPM_LIFECYCLE_SCRIPTS:
            return second
        return None
    builtins = {
        "pnpm": _PNPM_BUILTINS,
        "yarn": _YARN_BUILTINS,
        "bun": _BUN_BUILTINS,
    }.get(head, frozenset())
    if second in builtins:
        return None
    return second


def _package_json_has_script(working_dir: Path, name: str) -> bool:
    try:
        raw = (working_dir / "package.json").read_text(encoding="utf-8")
    except OSError:
        return False
    try:
        data = json.loads(raw)
    except json.JSONDecodeError:
        return False
    scripts = data.get("scripts") if isinstance(data, dict) else None
    return isinstance(scripts, dict) and name in scripts


def _node_workspace_filter(head: str, parts: list[str]) -> str | None:
    """Return the workspace name targeted by a PM invocation, if any.

    Recognises ``--filter <name>`` / ``--filter=<name>`` / ``-F <name>``
    (pnpm), ``--workspace <name>`` / ``-w <name>`` (npm),
    ``yarn workspace <name>``, and
    ``yarn workspaces foreach [flags] --include <name> <script>``.
    Returns ``None`` when no workspace filter is present.
    """
    i = 1
    while i < len(parts):
        token = parts[i]
        if token in {"--filter", "-F", "--workspace", "-w", "--include"} and i + 1 < len(parts):
            return parts[i + 1]
        for prefix in ("--filter=", "--workspace=", "--include="):
            if token.startswith(prefix):
                return token[len(prefix) :]
        if head == "yarn" and token == "workspace" and i + 1 < len(parts):
            return parts[i + 1]
        # ``yarn workspaces foreach --include <name> <script>``: the filter
        # is the ``--include`` flag; the ``workspaces`` keyword itself does
        # not carry the name.
        i += 1
    return None


def _node_workspace_has_script(
    working_dir: Path,
    workspace_filter: str | None,
    script_name: str,
) -> bool:
    """Check whether ``script_name`` is declared in the targeted package.json.

    When a workspace filter is supplied, we walk the common monorepo layouts
    (``<name>/``, ``packages/<name>/``, ``apps/<name>/``) and the
    ``package.json.workspaces`` / ``pnpm-workspace.yaml`` globs, validating
    against the first matching package.json. Without a filter the root
    ``package.json`` is authoritative.
    """
    if workspace_filter is None:
        return _package_json_has_script(working_dir, script_name)
    for candidate in _iter_workspace_package_dirs(working_dir, workspace_filter):
        if _package_json_has_script(candidate, script_name):
            return True
    return False


def _iter_workspace_package_dirs(working_dir: Path, name: str):
    """Yield directories that could house the workspace package ``name``.

    The search is intentionally conservative: it covers the flat root,
    ``packages/`` and ``apps/`` conventions, and the declared globs in
    ``package.json.workspaces`` / ``pnpm-workspace.yaml``. Globs like
    ``packages/*`` are expanded with ``Path.glob`` so the workspace's own
    ``package.json`` name can be inspected to confirm the match.
    """
    for relative in (name, f"packages/{name}", f"apps/{name}"):
        candidate = _resolve_within(working_dir, relative)
        if candidate is not None and (candidate / "package.json").is_file():
            yield candidate
    try:
        root_data = json.loads((working_dir / "package.json").read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        root_data = None
    globs: list[str] = []
    if isinstance(root_data, dict):
        raw_ws = root_data.get("workspaces")
        if isinstance(raw_ws, list):
            globs.extend(g for g in raw_ws if isinstance(g, str))
        elif isinstance(raw_ws, dict):
            packages = raw_ws.get("packages")
            if isinstance(packages, list):
                globs.extend(g for g in packages if isinstance(g, str))
    pnpm_ws = working_dir / "pnpm-workspace.yaml"
    if pnpm_ws.is_file():
        try:
            for line in pnpm_ws.read_text(encoding="utf-8").splitlines():
                stripped = line.strip()
                if stripped.startswith("- "):
                    entry = stripped[2:].strip().strip("'\"")
                    if entry:
                        globs.append(entry)
        except OSError:
            pass
    seen: set[Path] = set()
    for pattern in globs:
        if not _relative_glob_stays_within(pattern):
            continue
        try:
            for candidate in working_dir.glob(pattern):
                if not (candidate / "package.json").is_file():
                    continue
                key = candidate.resolve()
                if not _path_is_within(working_dir, key):
                    continue
                if key in seen:
                    continue
                seen.add(key)
                try:
                    body = json.loads((candidate / "package.json").read_text(encoding="utf-8"))
                except (OSError, json.JSONDecodeError):
                    continue
                ws_name = body.get("name") if isinstance(body, dict) else None
                if ws_name == name or candidate.name == name:
                    yield candidate
        except (OSError, ValueError):
            continue


def _justfile_has_recipe(working_dir: Path, recipe: str) -> bool:
    """True when ``recipe`` is declared at column 0 in a project justfile.

    Parses the justfile line-by-line looking for ``recipe-name[ args]:`` at
    the start of a line (optionally prefixed with ``@`` for quiet recipes).
    Body lines are indented with a tab or spaces and are skipped.
    """
    import re as _re

    recipe_pattern = _re.compile(r"^\s*@?([A-Za-z_][A-Za-z0-9_\-]*)\b[^:\n]*:")
    for name in ("justfile", "Justfile", ".justfile"):
        path = working_dir / name
        if not path.is_file():
            continue
        try:
            lines = path.read_text(encoding="utf-8").splitlines()
        except OSError:
            continue
        for line in lines:
            if not line or line.startswith((" ", "\t", "#")):
                continue
            match = recipe_pattern.match(line)
            if match and match.group(1) == recipe:
                return True
    return False


def _make_target(parts: list[str]) -> str | None:
    """Return the first positional target in a ``make`` invocation."""
    for token in parts[1:]:
        if token.startswith("-"):
            continue
        if "=" in token:  # VAR=value overrides are not targets
            continue
        return token
    return None


def _makefile_has_target(working_dir: Path, target: str) -> bool:
    for name in ("Makefile", "makefile", "GNUmakefile"):
        path = working_dir / name
        if not path.is_file():
            continue
        try:
            for line in path.read_text(encoding="utf-8").splitlines():
                stripped = line.strip()
                if not stripped or stripped.startswith("#"):
                    continue
                # Match `target:` or `target :` at line start, skipping `.PHONY`.
                head = stripped.split(":", 1)[0].strip()
                if head == target:
                    return True
                # `.PHONY: build test` declarations also satisfy the check.
                if head == ".PHONY":
                    declared = stripped.split(":", 1)[1]
                    if target in declared.split():
                        return True
        except OSError:
            continue
    return False


def _wrapper_invocation_is_runnable(raw_head: str, basename: str) -> bool:
    """True when a build wrapper can actually be executed as-written.

    ``create_subprocess_exec`` performs a straight ``execvp`` without
    consulting the current working directory, so bare ``mvnw`` /
    ``gradlew`` from the repo root silently fail on Unix unless ``.`` is
    on ``PATH``. Require the explicit ``./`` prefix. ``.cmd`` / ``.bat``
    launchers are Windows-only and are refused on POSIX hosts where they
    would not be executable anyway.
    """
    if basename.endswith((".cmd", ".bat")):
        # ``.cmd`` / ``.bat`` launchers are Windows-only; on Windows both
        # ``mvnw.cmd`` and ``.\mvnw.cmd`` resolve via cmd.exe semantics.
        return os.name == "nt"
    # Unix-style wrapper (``mvnw`` / ``gradlew``): must be invoked via
    # ``./mvnw`` so the OS resolves it without a ``PATH`` lookup.
    return raw_head.startswith("./")


def _has_any(working_dir: Path, names: tuple[str, ...]) -> bool:
    """True when any of ``names`` exists as a file in ``working_dir``."""
    return any((working_dir / name).is_file() for name in names)


def _package_json_declares_dependency(working_dir: Path, name: str) -> bool:
    """True when ``name`` appears in any ``package.json`` dependency section."""
    path = working_dir / "package.json"
    if not path.is_file():
        return False
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return False
    if not isinstance(data, dict):
        return False
    for section in (
        "dependencies",
        "devDependencies",
        "optionalDependencies",
        "peerDependencies",
    ):
        deps = data.get(section)
        if isinstance(deps, dict) and name in deps:
            return True
    return False


def _requirements_files_declare(working_dir: Path, name: str) -> bool:
    """True when ``name`` appears in any ``requirements*.txt`` in the repo root."""
    lowered = name.lower()
    try:
        candidates = sorted(working_dir.glob("requirements*.txt"))
    except OSError:
        return False
    for path in candidates:
        try:
            content = path.read_text(encoding="utf-8")
        except OSError:
            continue
        for raw in content.splitlines():
            stripped = raw.strip()
            if not stripped or stripped.startswith("#") or stripped.startswith("-"):
                continue
            token = stripped.split(";", 1)[0].strip()
            for sep in "[<>=!~ ":
                idx = token.find(sep)
                if idx != -1:
                    token = token[:idx]
                    break
            if token.strip().lower() == lowered:
                return True
    return False


# Python standard-library modules that are safe, zero-cost Stage 1 entries
# via ``python -m <module>``. This list is intentionally conservative —
# modules that would start network services or mutate state (``pip``,
# ``venv``, ``http.server`` …) are excluded. Third-party modules are only
# accepted when they are declared as a project dependency.
_PYTHON_STDLIB_RUNNABLE_MODULES: frozenset[str] = frozenset(
    {
        "unittest",
        "doctest",
        "compileall",
        "pydoc",
        "json.tool",
        "pickletools",
        "timeit",
        "tabnanny",
        "py_compile",
        "zipfile",
        "zipapp",
        "dis",
        "ast",
        "cProfile",
    }
)


def _python_module_is_available(working_dir: Path, parts: list[str]) -> bool:
    """Validate ``python -m <module>`` forms against repo state.

    Plain ``python`` without ``-m`` is too open-ended to verify, so Stage 1
    rejects it. ``python -m <stdlib>`` is accepted for a curated safe list;
    other modules must be a declared dependency or vendored in ``.venv``.
    """
    if "-m" not in parts:
        return False
    try:
        module_idx = parts.index("-m") + 1
    except ValueError:
        return False
    if module_idx >= len(parts):
        return False
    module = parts[module_idx]
    if module.startswith("-"):
        return False
    top = module.split(".", 1)[0]
    if module in _PYTHON_STDLIB_RUNNABLE_MODULES or top in _PYTHON_STDLIB_RUNNABLE_MODULES:
        return True
    if _bare_tool_declared_by_repo(working_dir, top):
        return True
    # Vendored into the project venv (e.g. ``.venv/lib/python3.12/site-packages/<mod>``).
    for venv_dir in (working_dir / ".venv", working_dir / "venv"):
        if not venv_dir.is_dir():
            continue
        try:
            for lib in venv_dir.glob("lib/python*/site-packages"):
                if (lib / top).exists() or (lib / f"{top}.py").exists():
                    return True
        except OSError:
            continue
    return False


def _bare_tool_declared_by_repo(working_dir: Path, tool: str) -> bool:
    """True when a bare tool (pytest, ruff, eslint…) is provisioned by the repo.

    Accepts any of:
      * Project-local bin: ``.venv/bin/<tool>`` or ``node_modules/.bin/<tool>``
      * Python: declared in ``pyproject.toml`` or ``requirements*.txt``
      * Node: declared in ``package.json`` dependencies
    """
    if (working_dir / ".venv" / "bin" / tool).exists():
        return True
    if (working_dir / ".venv" / "Scripts" / f"{tool}.exe").exists():
        return True
    if (working_dir / "node_modules" / ".bin" / tool).exists():
        return True
    if _pyproject_declares_dependency(working_dir, tool):
        return True
    if _package_json_declares_dependency(working_dir, tool):
        return True
    return _requirements_files_declare(working_dir, tool)


def _gmake_validator(working_dir: Path, parts: list[str]) -> bool:
    """Same contract as ``make``: require an explicit target.

    Bare ``gmake`` would run whatever the first Makefile rule is (often
    ``install`` / ``deploy``), so we refuse the command and force callers to
    name the target explicitly.
    """
    if not _has_any(working_dir, ("Makefile", "makefile", "GNUmakefile")):
        return False
    target = _make_target(parts)
    if target is None:
        return False
    if _is_destructive_target(target):
        return False
    return _makefile_has_target(working_dir, target)


# Non-mutating dotnet subcommands. ``publish`` / ``pack`` / ``nuget push`` /
# ``run`` / ``restore`` / ``new`` mutate either project artifacts, the package
# cache, or external feeds and must never be persisted as Stage 1 commands.
# ``dotnet msbuild`` exposes arbitrary MSBuild ``/t:<target>`` switches
# (``Publish`` / ``Pack`` / ``Restore`` …) which can mutate state or publish
# artifacts; parsing MSBuild target lists is out of scope for the validator,
# so it is deliberately omitted. Users who need ``msbuild`` can author the
# command into ``mechanical.toml`` manually.
_DOTNET_SAFE_SUBCOMMANDS: frozenset[str] = frozenset(
    {
        "build",
        "test",
        "format",
        "list",
    }
)


_DOTNET_PROJECT_SUFFIXES: tuple[str, ...] = (".csproj", ".sln", ".fsproj", ".vbproj")


def _dotnet_project_markers_exist(working_dir: Path) -> bool:
    if (working_dir / "global.json").is_file():
        return True
    try:
        for pattern in (f"*{suffix}" for suffix in _DOTNET_PROJECT_SUFFIXES):
            if any(working_dir.glob(pattern)):
                return True
    except OSError:
        return False
    return False


def _dotnet_project_argument_exists(working_dir: Path, parts: list[str], subcommand: str) -> bool:
    """True when a safe dotnet command names an in-repo project/solution file."""

    try:
        start = parts.index(subcommand) + 1
    except ValueError:
        return False
    for token in parts[start:]:
        for candidate in _argument_path_candidates(token):
            if not candidate.endswith(_DOTNET_PROJECT_SUFFIXES):
                continue
            path = _resolve_within(working_dir, candidate)
            if path is not None and path.is_file():
                return True
    return False


def _dotnet_validator(working_dir: Path, parts: list[str]) -> bool:
    sub = _first_positional(parts)
    if sub is None:
        return False
    if sub not in _DOTNET_SAFE_SUBCOMMANDS:
        return False
    return _dotnet_project_markers_exist(working_dir) or _dotnet_project_argument_exists(
        working_dir, parts, sub
    )


# Runners whose behavior depends on project-local config files. Each entry
# must prove the relevant manifest/target exists before the command is
# accepted, closing the "host has binary but repo cannot actually run it"
# phantom-failure window.
# Runners whose *existence* on disk is enough: they either wrap fixed
# manifests (``ant`` → ``build.xml``) or drive their own discovery
# (``bazel``, ``cmake``). Runners that take a user-controlled target name
# (``poetry run``, ``gradle <task>``, ``task <name>``, ``rake <task>``,
# ``composer <script>``, ``bundle exec``, ``mix <task>``) are validated with
# dedicated helpers below because manifest presence alone is not proof.
_REPO_COUPLED_RUNNERS: dict[str, Callable[[Path, list[str]], bool]] = {
    "gmake": _gmake_validator,
    "ant": lambda wd, _parts: (wd / "build.xml").is_file(),
    "tox": lambda wd, _parts: (wd / "tox.ini").is_file() or (wd / "pyproject.toml").is_file(),
    "nox": lambda wd, _parts: (wd / "noxfile.py").is_file(),
    "bazel": lambda wd, _parts: _has_any(
        wd,
        ("WORKSPACE", "WORKSPACE.bazel", "MODULE.bazel", "BUILD", "BUILD.bazel"),
    ),
    "buck": lambda wd, _parts: _has_any(wd, (".buckconfig", "BUCK")),
    "cmake": lambda wd, _parts: (wd / "CMakeLists.txt").is_file(),
    "ninja": lambda wd, _parts: _has_any(wd, ("build.ninja", "CMakeLists.txt")),
    "rubocop": lambda wd, _parts: _has_any(wd, (".rubocop.yml", ".rubocop.yaml", "Gemfile")),
    "phpunit": lambda wd, _parts: _has_any(
        wd, ("phpunit.xml", "phpunit.xml.dist", "composer.json")
    ),
    "dotnet": _dotnet_validator,
}


def _python_pm_run_tool_is_declared(working_dir: Path, parts: list[str]) -> bool:
    """Validate ``poetry run`` / ``pdm run`` / ``hatch run`` invocations.

    The tool invoked must be a declared project dependency or installed
    into the project venv. Non-``run`` subcommands (``poetry lock``,
    ``pdm install``, ``hatch build`` …) mutate project state and are
    dropped so they never land in ``mechanical.toml`` as Stage 1 commands.
    """
    sub = _first_positional(parts)
    if sub != "run":
        return False
    try:
        run_idx = parts.index("run")
    except ValueError:
        return False
    for token in parts[run_idx + 1 :]:
        if token.startswith("-"):
            continue
        return _bare_tool_declared_by_repo(working_dir, token.split("@", 1)[0])
    return False


_GRADLE_BUILTIN_TASKS: frozenset[str] = frozenset(
    {
        "assemble",
        "build",
        "check",
        "clean",
        "dependencies",
        "help",
        "jar",
        "javadoc",
        "projects",
        "properties",
        "tasks",
        "test",
    }
)


def _gradle_task_is_declared(working_dir: Path, parts: list[str]) -> bool:
    """Validate a ``gradle <task>`` / ``./gradlew <task>`` invocation.

    Accepts either a curated set of core-plugin lifecycle tasks (``build``,
    ``check``, ``test`` …) or a task declared in ``build.gradle`` /
    ``build.gradle.kts`` via the common registration forms
    (``task <name> {…}``, ``tasks.register("<name>")``,
    ``tasks.create("<name>")``). Task names that look destructive
    (``publish``, ``deploy``, ``release`` …) are refused even when
    declared — Stage 1 is not the right place to run them.
    """
    import re as _re

    task = _first_positional(parts)
    if task is None:
        return False
    if _is_destructive_target(task):
        return False
    if task in _GRADLE_BUILTIN_TASKS:
        return True
    declared: set[str] = set()
    for name in ("build.gradle", "build.gradle.kts"):
        path = working_dir / name
        if not path.is_file():
            continue
        try:
            text = path.read_text(encoding="utf-8")
        except OSError:
            continue
        declared.update(_re.findall(r"\btask\s+([A-Za-z_][A-Za-z0-9_]*)\b", text))
        declared.update(
            _re.findall(
                r"\btasks\s*\.\s*(?:register|create|named)\s*\(\s*['\"]([A-Za-z_][A-Za-z0-9_]*)",
                text,
            )
        )
    return task in declared


def _task_task_is_declared(working_dir: Path, parts: list[str]) -> bool:
    """Validate a ``task <name>`` (go-task) invocation against ``Taskfile.yml``."""
    import re as _re

    name = _first_positional(parts)
    if name is None:
        return False
    if _is_destructive_target(name):
        return False
    for candidate in ("Taskfile.yml", "Taskfile.yaml", "taskfile.yml", "taskfile.yaml"):
        path = working_dir / candidate
        if not path.is_file():
            continue
        try:
            text = path.read_text(encoding="utf-8")
        except OSError:
            continue
        # ``tasks:`` block keys; each task name appears as ``  <name>:`` at
        # the same indent level — a simple regex is sufficient for the
        # common layout without pulling a YAML parser into the hot path.
        in_tasks = False
        for line in text.splitlines():
            stripped = line.rstrip()
            if not stripped or stripped.lstrip().startswith("#"):
                continue
            if not stripped.startswith((" ", "\t")):
                in_tasks = stripped.strip() == "tasks:"
                continue
            if not in_tasks:
                continue
            match = _re.match(r"^[ \t]+([A-Za-z_][A-Za-z0-9_\-]*)\s*:", line)
            if match and match.group(1) == name:
                return True
    return False


def _rake_task_is_declared(working_dir: Path, parts: list[str]) -> bool:
    """Validate a ``rake <task>`` invocation against ``Rakefile`` declarations."""
    import re as _re

    task = _first_positional(parts)
    if task is None:
        return False
    if _is_destructive_target(task):
        return False
    for candidate in ("Rakefile", "rakefile"):
        path = working_dir / candidate
        if not path.is_file():
            continue
        try:
            text = path.read_text(encoding="utf-8")
        except OSError:
            continue
        if _re.search(rf"\btask\s+:({_re.escape(task)})\b", text):
            return True
        if _re.search(rf"\btask\s+['\"]({_re.escape(task)})['\"]", text):
            return True
    return False


def _bundle_subcommand_is_safe(working_dir: Path, parts: list[str]) -> bool:
    """Validate a ``bundle`` invocation.

    Accepts ``bundle exec <tool>`` only when ``<tool>`` is declared as a gem
    in ``Gemfile`` or present as a binstub in ``bin/``. Bare ``bundle`` with
    no subcommand falls through to require a Gemfile in place.
    """
    if not (working_dir / "Gemfile").is_file():
        return False
    sub = _first_positional(parts)
    if sub != "exec":
        return False
    try:
        exec_idx = parts.index("exec")
    except ValueError:
        return False
    for token in parts[exec_idx + 1 :]:
        if token.startswith("-"):
            continue
        # Refuse deploy-style gem binaries even when the gem is declared.
        if _is_destructive_target(token):
            return False
        if (working_dir / "bin" / token).is_file():
            return True
        try:
            gem_text = (working_dir / "Gemfile").read_text(encoding="utf-8")
        except OSError:
            return False
        import re as _re_bundle

        return bool(_re_bundle.search(rf"\bgem\s+['\"]({_re_bundle.escape(token)})['\"]", gem_text))
    return False


# Composer builtin subcommands. ``composer install`` / ``update`` / etc.
# mutate project state; even if a repo declares a ``scripts.install`` hook,
# Composer still executes the builtin behavior (it merely augments it), so
# these names must never be accepted as "safe custom scripts".
_COMPOSER_BUILTINS: frozenset[str] = frozenset(
    {
        "install",
        "update",
        "require",
        "remove",
        "create-project",
        "init",
        "global",
        "dump-autoload",
        "dumpautoload",
        "depends",
        "why",
        "why-not",
        "prohibits",
        "suggests",
        "archive",
        "exec",
        "config",
        "clear-cache",
        "clearcache",
        "search",
        "outdated",
        "check-platform-reqs",
        "status",
        "licenses",
        "bump",
        "audit",
        "reinstall",
        "self-update",
        "selfupdate",
    }
)


def _composer_script_is_declared(working_dir: Path, parts: list[str]) -> bool:
    """Validate a ``composer <script>`` invocation against ``composer.json``.

    The lookup goes through the declared ``scripts`` table, but builtin
    Composer subcommand names (``install`` / ``update`` / ``require`` …)
    are rejected up front. Composer runs those builtins first even when a
    same-named custom script exists, so letting them land in
    ``mechanical.toml`` would re-open the state-mutation path.
    """
    path = working_dir / "composer.json"
    if not path.is_file():
        return False
    sub = _first_positional(parts)
    if sub is None:
        return False
    if sub == "run-script":
        try:
            script_idx = parts.index("run-script") + 1
        except ValueError:
            return False
        if script_idx >= len(parts):
            return False
        script = parts[script_idx]
    else:
        if sub in _COMPOSER_BUILTINS:
            return False
        script = sub
    try:
        data = json.loads(path.read_text(encoding="utf-8"))
    except (OSError, json.JSONDecodeError):
        return False
    if not isinstance(data, dict):
        return False
    scripts = data.get("scripts")
    return isinstance(scripts, dict) and script in scripts


# True Mix builtins only. Third-party tasks like ``credo`` or ``dialyzer``
# are provided by optional dependencies and must be declared in ``mix.exs``
# to be runnable; the detector cannot prove that from the manifest excerpt
# alone, so those tasks can be added to ``mechanical.toml`` by hand but are
# not accepted by the AI-driven path.
_MIX_BUILTIN_TASKS: frozenset[str] = frozenset(
    {
        "compile",
        "format",
        "test",
        "help",
        "docs",
    }
)


def _mix_task_is_builtin(working_dir: Path, parts: list[str]) -> bool:
    """Validate a ``mix <task>`` invocation against a safe builtin list."""
    if not (working_dir / "mix.exs").is_file():
        return False
    task = _first_positional(parts)
    if task is None:
        return False
    return task in _MIX_BUILTIN_TASKS


def _render_toml(commands: DetectedCommands) -> str:
    """Serialize validated commands into ``mechanical.toml`` body.

    Values are written as TOML basic strings with ``\\`` and ``"`` escaped so
    commands like ``pytest -k "slow"`` survive the round-trip intact.
    """
    lines = [_DETECT_MARKER, ""]
    for key in _COMMAND_KEYS:
        value = getattr(commands, key)
        if value is None:
            continue
        lines.append(f"{key} = {_toml_basic_string(value)}")
    lines.append("")
    return "\n".join(lines)


_TOML_ESCAPES: dict[str, str] = {
    "\\": "\\\\",
    '"': '\\"',
    "\b": "\\b",
    "\t": "\\t",
    "\n": "\\n",
    "\f": "\\f",
    "\r": "\\r",
}


def _toml_basic_string(value: str) -> str:
    """Quote ``value`` as a TOML basic string with mandatory escapes."""
    out = [_TOML_ESCAPES.get(ch, ch) for ch in value]
    return '"' + "".join(out) + '"'


def _redact(proposal: dict[str, str]) -> dict[str, str]:
    """Truncate proposal values for safer logging."""
    return {k: (v[:120] if isinstance(v, str) else str(v)) for k, v in proposal.items()}


